package com.example.sistema.model;

public class RegistroXp {
    private int dia;
    private int xp;

    public RegistroXp(int dia, int xp) {
        this.dia = dia;
        this.xp = xp;
    }

    public int getDia() {
        return dia;
    }

    public int getXp() {
        return xp;
    }
}
